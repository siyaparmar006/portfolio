import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = join(import.meta.dirname, "..", "public", "assets", "projects");
const covers = {};
const galleries = {};

for (const slug of readdirSync(root)) {
  const dir = join(root, slug);
  if (!statSync(dir).isDirectory()) continue;

  for (const file of readdirSync(dir)) {
    if (file === "cover.jpg") {
      covers[slug] = `/assets/projects/${slug}/cover.jpg`;
    }
    if (/^gallery-\d+\.jpg$/i.test(file)) {
      if (!galleries[slug]) galleries[slug] = [];
      galleries[slug].push(`/assets/projects/${slug}/${file}`);
    }
  }

  if (galleries[slug]) {
    galleries[slug].sort((a, b) => a.localeCompare(b));
  }
}

const out = `// Auto-generated by scripts/generate-asset-manifest.mjs — do not edit manually.
export const projectCovers: Record<string, string> = ${JSON.stringify(covers, null, 2)};

export const projectGalleries: Record<string, string[]> = ${JSON.stringify(galleries, null, 2)};
`;

writeFileSync(join(import.meta.dirname, "..", "src", "content", "asset-manifest.ts"), out);
console.log(`covers: ${Object.keys(covers).length}, galleries: ${Object.keys(galleries).length}`);
